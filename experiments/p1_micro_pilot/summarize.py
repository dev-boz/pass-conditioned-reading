"""Machine-generate transcripts/OBSERVATIONS.md from p1_run.log (D24: every
mean states its formula and is computed, not transcribed).

Usage: uv run python experiments/p1_micro_pilot/summarize.py
"""

from __future__ import annotations

import re
import sys
from datetime import date
from pathlib import Path

HERE = Path(__file__).resolve().parent
ROOT = HERE.parents[1]

PASS_RE = re.compile(r"\[(\w+)\] pass (\d+)/(\d+) \((\w+)\) . prompting \((\d+) tok\)")
OPS_RE = re.compile(r"ops (\d+)/(\d+) valid, (\d+) applied; state (\d+) tok( \[over state budget\])? \(([\d.]+)s\)")


def main() -> None:
    log = (ROOT / "p1_run.log").read_text(encoding="utf-8", errors="replace")
    rows: dict[str, list[dict]] = {}
    cur = None
    for line in log.splitlines():
        m = PASS_RE.search(line)
        if m:
            cur = {"arm": m.group(1), "k": int(m.group(2)), "K": int(m.group(3)),
                   "stage": m.group(4), "prompt_tok": int(m.group(5))}
            continue
        m = OPS_RE.search(line)
        if m and cur:
            cur.update(valid=int(m.group(1)), cand=int(m.group(2)), applied=int(m.group(3)),
                       state_tok=int(m.group(4)), over=bool(m.group(5)), wall_s=float(m.group(6)))
            rows.setdefault(cur["arm"], []).append(cur)
            cur = None

    arms = list(rows)
    if not arms:
        sys.exit("no complete pass records found in p1_run.log")
    K = rows[arms[0]][0]["K"]

    def fmt_mean(x: float) -> str:
        return f"{x:.3f}"

    L = [
        "# P1 — mechanical observations (facts only; the qualitative judgment is the maintainer's)",
        "",
        f"Machine-generated by `summarize.py` from `p1_run.log` on {date.today()} — parser v2.1 (D22).",
        "Doc `govreport-00055` (8 456 tok) · K=6 · Qwen2.5-7B-Instruct Q4_K_M, llama.cpp CPU, temp 0, seed 42",
        "",
        "## Per-pass edit-op validity (valid/candidate lines)",
        "",
        "| pass | " + " | ".join(arms) + " |",
        "|---|" + "---|" * len(arms),
    ]
    for k in range(1, K + 1):
        cells = []
        for a in arms:
            r = next((x for x in rows[a] if x["k"] == k), None)
            cells.append(f"{r['valid']}/{r['cand']}" if r else "—")
        L.append(f"| {k} | " + " | ".join(cells) + " |")
    micro = {a: sum(r["valid"] for r in rows[a]) / max(sum(r["cand"] for r in rows[a]), 1) for a in arms}
    macro = {a: sum((r["valid"] / r["cand"] if r["cand"] else 0.0) for r in rows[a]) / len(rows[a]) for a in arms}
    L += [
        "| **micro mean** | " + " | ".join(fmt_mean(micro[a]) for a in arms) + " |",
        "| **macro mean** | " + " | ".join(fmt_mean(macro[a]) for a in arms) + " |",
        "",
        "Formulas (D24): **micro** = Σ valid / Σ candidate lines over the arm's "
        f"{K} passes (op-weighted); **macro** = unweighted mean over passes of "
        "(valid/candidate), passes with 0 candidate lines counted as rate 0.",
        "",
        "## Integration-state size after each pass (tokens; budget 1 800, D16)",
        "",
        "| pass | " + " | ".join(arms) + " |",
        "|---|" + "---|" * len(arms),
    ]
    for k in range(1, K + 1):
        cells = []
        for a in arms:
            r = next((x for x in rows[a] if x["k"] == k), None)
            cells.append((f"{r['state_tok']}" + (" ⚠" if r["over"] else "")) if r else "—")
        L.append(f"| {k} | " + " | ".join(cells) + " |")
    L += [
        "",
        "## Cost (Σ prompt tokens from the runner's pre-call counts; wall-clock = Σ per-pass generation time)",
        "",
        "| arm | prompt tok (Σ) | wall-clock (Σ, min) | per-pass range (s) |",
        "|---|---|---|---|",
    ]
    for a in arms:
        pt = sum(r["prompt_tok"] for r in rows[a])
        w = [r["wall_s"] for r in rows[a]]
        L.append(f"| {a} | {pt:,} | {sum(w) / 60:.1f} | {min(w):.0f}–{max(w):.0f} |")
    L += [
        "",
        "P1 is deliberately **not** compute-matched (that is E2′'s budget definition); "
        "the full-resolution arm's prefill cost is the parent's bounded-per-pass property shown concretely.",
        "",
    ]
    out = HERE / "transcripts" / "OBSERVATIONS.md"
    out.write_text("\n".join(L), encoding="utf-8")
    print(f"wrote {out}")
    for a in arms:
        print(f"  {a}: micro={fmt_mean(micro[a])} macro={fmt_mean(macro[a])}")


if __name__ == "__main__":
    main()
